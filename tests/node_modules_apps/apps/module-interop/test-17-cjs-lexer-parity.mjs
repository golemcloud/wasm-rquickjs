import assert from 'node:assert';
import objectLiteralDefault, {
  a as literalA,
  b as literalB,
  d as literalD,
  depAlpha as literalDepAlpha,
  depBeta as literalDepBeta,
} from 'cjs-lexer-object-literal';
import callbackDefault, {
  depAlpha as callbackDepAlpha,
  depBeta as callbackDepBeta,
  own as callbackOwn,
} from 'cjs-lexer-keys-reexport';
import requireValueDefault, {
  a as requireValueA,
  b as requireValueB,
  depAlpha as requireValueDepAlpha,
} from 'cjs-lexer-object-require-value';
import * as requireValueNs from 'cjs-lexer-object-require-value';

export const run = () => {
  assert.strictEqual(literalA, 1);
  assert.strictEqual(literalB, 2);
  assert.strictEqual(literalD, 4);
  assert.strictEqual(literalDepAlpha, 'dep-alpha');
  assert.strictEqual(literalDepBeta, 'dep-beta');
  assert.strictEqual(objectLiteralDefault.depAlpha, 'dep-alpha');

  assert.strictEqual(callbackDepAlpha, 'dep-alpha');
  assert.strictEqual(callbackDepBeta, 'dep-beta');
  assert.strictEqual(callbackOwn, 'own-value');
  assert.strictEqual(callbackDefault.own, 'own-value');

  assert.strictEqual(requireValueA, 1);
  assert.deepStrictEqual(requireValueB, { depAlpha: 'dep-alpha' });
  assert.strictEqual(requireValueDepAlpha, undefined);
  assert.strictEqual(Object.hasOwn(requireValueNs, 'depAlpha'), true);
  assert.strictEqual(requireValueDefault.afterRequire, 3);
  assert.strictEqual(Object.hasOwn(requireValueNs, 'afterRequire'), false);

  return 'PASS: CJS lexer parity object-literal and keys reexport patterns';
};
