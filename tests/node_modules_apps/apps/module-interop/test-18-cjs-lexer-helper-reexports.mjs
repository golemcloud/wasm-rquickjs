import assert from 'node:assert';
import helperDefault, {
  depAlpha,
  depBeta,
  depGamma,
  depDelta,
  own as helperOwn,
} from 'cjs-lexer-helper-reexports';

const negativeNs = await import('cjs-lexer-helper-reexports-negative');

export const run = () => {
  assert.strictEqual(depAlpha, 'dep-alpha');
  assert.strictEqual(depBeta, 'dep-beta');
  assert.strictEqual(depGamma, 'dep-gamma');
  assert.strictEqual(depDelta, 'dep-delta');
  assert.strictEqual(helperOwn, 'own-value');
  assert.strictEqual(helperDefault.depDelta, 'dep-delta');

  assert.strictEqual(negativeNs.default.depNested, 'dep-nested');
  assert.strictEqual(negativeNs.default.depDynamic, 'dep-dynamic');
  assert.strictEqual(Object.hasOwn(negativeNs, 'depNested'), false);
  assert.strictEqual(Object.hasOwn(negativeNs, 'depDynamic'), false);
  assert.strictEqual(Object.hasOwn(negativeNs, 'own'), true);

  return 'PASS: CJS lexer parity helper reexport patterns';
};
