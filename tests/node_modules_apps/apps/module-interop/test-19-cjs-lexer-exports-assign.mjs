import assert from 'node:assert';
import assignDefault, {
  depAlpha,
  depBeta,
  depGamma,
  own,
} from 'cjs-lexer-exports-assign';

const negativeNs = await import('cjs-lexer-exports-assign-negative');

export const run = () => {
  assert.strictEqual(depAlpha, 'dep-alpha');
  assert.strictEqual(depBeta, 'dep-beta');
  assert.strictEqual(depGamma, 'dep-gamma');
  assert.strictEqual(own, 'own-value');
  assert.strictEqual(assignDefault.depAlpha, 'dep-alpha');
  assert.strictEqual(assignDefault.depGamma, 'dep-gamma');

  assert.strictEqual(negativeNs.default.depDynamic, 'dep-dynamic');
  assert.strictEqual(negativeNs.default.depStatic, 'dep-static');
  assert.strictEqual(Object.hasOwn(negativeNs, 'depDynamic'), false);
  assert.strictEqual(Object.hasOwn(negativeNs, 'depStatic'), false);
  assert.strictEqual(Object.hasOwn(negativeNs, 'own'), true);

  return 'PASS: CJS lexer parity EXPORTS_ASSIGN and export-star guard patterns';
};
