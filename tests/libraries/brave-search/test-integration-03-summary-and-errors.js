import assert from 'assert';
import { BraveSearchError, createClient, setBaseUrl } from './helpers.js';

export const run = async () => {
  const summaryClient = createClient('test-brave-key', { pollInterval: 1, maxPollAttempts: 5 });
  setBaseUrl(summaryClient, 'http://localhost:18080/res/v1');

  const { webSearch, summary } = summaryClient.getSummarizedAnswer('summary flow', {
    count: 3,
    summary: true,
  });

  const webResult = await webSearch;
  const summaryResult = await summary;

  assert.strictEqual(webResult.query.original, 'summary flow');
  assert.strictEqual(summaryResult.status, 'complete');
  assert.strictEqual(summaryResult.summary[0].data, 'Mock summary generated by brave-search tests');

  const invalidKeyClient = createClient('wrong-key');
  setBaseUrl(invalidKeyClient, 'http://localhost:18080/res/v1');

  await assert.rejects(
    () => invalidKeyClient.webSearch('anything'),
    (error) => error instanceof BraveSearchError && error.message === 'Authentication error: Mock invalid subscription token'
  );

  await assert.rejects(
    () => summaryClient.webSearch('trigger-rate-limit'),
    (error) => error instanceof BraveSearchError && error.message === 'Rate limit exceeded: Mock request limit reached'
  );

  return 'PASS: summarized search polling and API error mapping work over real HTTP';
};
